SELECT j.JOB_TITLE `Cargo`, 
  ROUND(AVG(e.SALARY), 2) `Média salarial`, CASE
  WHEN ROUND(AVG(e.SALARY), 2) BETWEEN 2000 AND 5800 THEN 'Júnior'
  WHEN ROUND(AVG(e.SALARY), 2) BETWEEN 5800 AND 7500 THEN 'Pleno'
  WHEN ROUND(AVG(e.SALARY), 2) BETWEEN 7500 AND 10500 THEN 'Sênior'
  ELSE 'CEO'
END Senioridade
FROM hr.employees e
INNER JOIN hr.jobs j ON e.JOB_ID = j.JOB_ID
GROUP BY `Cargo`
ORDER BY `Média salarial`, `Cargo`;
