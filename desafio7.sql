SELECT 
DISTINCT UCASE(CONCAT(EMP.FIRST_NAME, ' ', EMP.LAST_NAME)) AS 'Nome completo',
JB_HIS.START_DATE AS 'Data de início',
EMP.SALARY AS 'Salário'
FROM hr.job_history AS JB_HIS
INNER JOIN hr.employees AS EMP
ON JB_HIS.EMPLOYEE_ID = EMP.EMPLOYEE_ID
WHERE MONTH(JB_HIS.START_DATE) BETWEEN 1 AND 3
ORDER BY UCASE(CONCAT(EMP.FIRST_NAME, ' ', EMP.LAST_NAME)) ASC, JB_HIS.START_DATE ASC;