SELECT
t1.JOB_TITLE Cargo, 
ROUND(AVG(t2.SALARY), 2) `Média salarial`, 
CASE
WHEN ROUND(AVG(t2.SALARY), 2) BETWEEN 2000 AND 5800 THEN 'Júnior'
WHEN ROUND(AVG(t2.SALARY), 2) BETWEEN 5800 AND 7500 THEN 'Pleno'
WHEN ROUND(AVG(t2.SALARY), 2) BETWEEN 7500 AND 10500 THEN 'Sênior'
ELSE 'CEO'
END Senioridade
FROM hr.employees t2
INNER JOIN hr.jobs t1 ON t2.JOB_ID = t1.JOB_ID
GROUP BY Cargo
ORDER BY `Média salarial`, Cargo;
